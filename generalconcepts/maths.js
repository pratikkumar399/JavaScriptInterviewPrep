// math object built in javascript

console.log(Math.PI); // 3.141592653589793

console.log(Math.E); // 2.718281828459045

console.log(Math.round(2.4)); // -> 2 rounds to nearest integer

console.log(Math.floor(2.6)); // -> 2 rounds to nearest integer

console.log(Math.ceil(2.4)); // -> 3 rounds to nearest greater integer